import java.util.Scanner;

public class test{

    public static void main(String[] args){
        Scanner input = new Scanner(System.in);

        System.out.println("enter n value Bugga:");
        int n = input.nextInt();

        System.out.println("the number at nth is: " + fib(n));
    }

    public static long fib(long n){
        if (n== 0)
            return 1;
        else if (n == 1)
            return 1;
        else
            return fib(n-1) + fib(n-2);
    }
}
